"""SQLite persistence for trades, signals, snapshots, and PDT tracking.

WHAT THIS MODULE DOES:
    Provides a thin, safe wrapper around SQLite for all data that needs
    to survive bot restarts: trade records, day-trade counts, daily
    equity snapshots, signal logs, options trades, and scanner results.

WHY IT EXISTS:
    The trading bot needs persistent storage for:
    1. Trade history — to calculate P&L, win rate, and other metrics.
    2. Day-trade records — to enforce the PDT rule across restarts.
    3. Daily snapshots — to compute drawdown and equity curves.
    4. Signal logs — to audit which signals were generated vs. acted on.
    5. Scanner results — to review what the screener found each morning.

    SQLite was chosen because it's file-based (no server to run), comes
    built into Python's standard library, and handles the modest write
    volumes of a single-account trading bot perfectly.

KEY DESIGN DECISIONS:
    - SQL injection prevention: all column names are validated against a
      regex before being interpolated into SQL.  Values are always passed
      as parameterized queries (the `?` placeholders), which SQLite's
      driver handles safely.
    - The @contextmanager pattern ensures database connections are always
      properly committed and closed, even if an error occurs.
    - Generic _insert, _update, _upsert methods use **kwargs to accept
      arbitrary columns, making it easy to add new fields without changing
      the method signatures.
    - The database file is stored at `<project_root>/data/ai_trade.db`.
      The directory is created automatically if it doesn't exist.

SCHEMA OVERVIEW:
    - trades:           Stock trade records (open, closed, pending).
    - day_trades:       PDT tracking records for the rolling 5-day window.
    - daily_snapshots:  End-of-day equity/cash snapshots for performance charts.
    - signals:          Log of every signal generated by strategies.
    - options_trades:   Options-specific trade records with Greeks and strikes.
    - scanner_results:  Pre-market screener output for each day.
"""

from __future__ import annotations

