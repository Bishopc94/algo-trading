# PYTHON PATTERN — @contextmanager:
# The `contextmanager` decorator lets you write a generator function that
# can be used with Python's `with` statement.  Code before the `yield`
# runs on entry, code after `yield` runs on exit (even if an error occurs).
# This is the standard pattern for resource management in Python (like
# try-with-resources in Java or using blocks in C#).
from contextlib import contextmanager
from pathlib import Path

